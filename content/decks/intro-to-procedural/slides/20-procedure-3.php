<h2>The Procedure</h2>
<div class="container">
	<div class="row">
		<ul>
			<li>A "sub-recipe", like chicken stock</li>
			<li>Standard piece of <dfn>procedural programming</dfn></li>
			<li>
				<p>In PHP you can define a <dfn>named procedure</dfn> like:</p>
				<div id="procedure-syntax-1" class="pretty-code non-editable" data-language="javascript" style="height:100px; width:50%;">
function helloWorld() {
	print 'Hello, World.';
}
				</div>
			</li>
			<li>
				<p>And later you can invoke it:</p>
				<div id="procedure-syntax-2" class="pretty-code non-editable" data-language="javascript" style="height:100px; width:50%;">
helloWorld();
				</div>
			</li>
			<li>
				This <em>"calls"</em> the procedure. It will run all the code within its <dfn>body</dfn> and then <dfn>return</dfn> control to the <em>"calling"</em> function.
			</li>
		</ul>
	</div>
</div>