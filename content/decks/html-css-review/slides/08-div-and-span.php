<div class="container slide">
	<p>What is special about <span class="token tag">div</span> and <span class="token tag">span</span>?</p>
	<div class="answer-container">
		<p>They have no inherent semantic meaning.</p>
	</div>
</div>
<div class="container slide">
	<p>What is the difference between <span class="token tag">div</span> and <span class="token tag">span</span>?</p>
	<div class="answer-container">
		<p>Span defaults to <span class="token css">display: inline</span> while div defaults to <span class="token css">display: block</span>.</p>
	</div>
</div>
<div class="container slide">
	<p>What is meant by the phrase <strong>content versus presentation</strong>?</p>
	<div class="answer-container">
		<p>
			Separating content from <em>how it is presented</em> is a fundamental best practice in web development, and really, programming in general.
		</p>
	</div>
</div>