<h2>Conceptual parts of HTML?</h2>
<p>What are the <em>conceptual</em> elements of an HTML document? In other words, what can you express or control with HTML?</p>
<ul>
	<li class="slide"><strong>content</strong> - text, images, figures, tables, accessibility</li>
	<li class="slide"><strong>structure</strong> - tag nesting, order in page, sectioning tags, links, anchors</li>
	<li class="slide"><strong>metadata</strong> - information about the content (e.g., <span class="token tag">&lt;link></span>, <span class="token tag">&lt;meta></span>, <span class="token tag">&lt;title></span>)</li>
</ul>

<div class="slide">
	<p>What are some things that mostly <em>aren't</em> addressed by HTML?</p>
	<ul>
		<li class="slide">interactivity</li>
		<li class="slide">embedded content formats (video, audio, images, plugins)</li>
		<li class="slide">appearance</li>
	</ul>
</div>

<div class="slide">
	<p>What <em>does</em> address those things?</p>
	<ul>
		<li class="slide">interactivity: server-side apps, client-side scripting languages</li>
		<li class="slide">embedded content: plugins, direct browser support</li>
		<li class="slide">appearance: CSS</li>
	</ul>
</div>
