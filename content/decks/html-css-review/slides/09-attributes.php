<h2>Attributes</h2>
<div class="container">
	<ul>
		<li>attributes are part of HTML language</li>
		<li>
			<code>&lt;<span class="token tag">tag</span> <span class="token attribute">attribute</span>="<span class="color-highlight">value</span>">content&lt;/<span class="token tag">tag</span>></code>
		</li>
		<li><a href="http://www.w3.org/html/wg/drafts/html/master/syntax.html#attributes-0">The spec on attributes</a></li>
		<li class="slide">two special attributes: <span class="token attribute">id</span> and <span class="token attribute">class</span></li>
	</ul>
	<div class="slide">
		<p>What is the difference between the <span class="token attribute">id</span> and <span class="token attribute">class</span> attributes?</p>
		<div class="answer-container">
			<p>
				<span class="token attribute">id</span>s should only be used on a single element within a page. <span class="token attribute">class</span>es can be used on any number of elements.
			</p>
		</div>
		<p>When would you use an <span class="token attribute">id</span> versus a <span class="token attribute">class</span>?</p>
		<div class="answer-container">
			<p>
				<span class="token attribute">class</span>es effectively extend the semantic range of the language. <span class="token attribute">id</span>s uniquely identify a single element. These things are useful both for scripting and styling.
			</p>
			<p>
				<span class="token attribute">id</span>s can also be used as the target for in-document anchor tags.
			</p>
		</div>
	</div>
</div>
