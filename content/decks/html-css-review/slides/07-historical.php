<h2>History</h2>
<div class="container">
	<ul>
		<li class="slide">Historically, HTML is hypertext-oriented</li>
		<li class="slide">Quickly became used/absued to do things it wasn't meant to do (e.g., using tables for visual layouts)</li>
		<li class="slide">Today, HTML 5 explicitly tries to address the ways the web is used <em>in practice</em></li>
	</ul>
	<blockquote class="slide">
	<p>The World Wide Web's markup language has always been HTML. HTML was primarily designed as a language for <strong>semantically describing scientific documents</strong>, although its general design and adaptations over the years have enabled it to be used to describe a number of other types of documents.</p>

	<p>The main area that has not been adequately addressed by HTML is a vague subject referred to as Web Applications. This standard attempts to rectify this, while at the same time updating the HTML language to address issues raised in the past few years.</p>
	<cite>- <a href="http://www.w3.org/html/wg/drafts/html/master/introduction.html#introduction">The HTML 5 spec</a>, emphasis mine</cite>
	</blockquote>
